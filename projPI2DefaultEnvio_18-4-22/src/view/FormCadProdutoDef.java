    private void carregarProduto() {
        //centraliza o cabeçalho da table
        ((DefaultTableCellRenderer) tblProduto.getTableHeader().getDefaultRenderer()).setHorizontalAlignment(SwingConstants.CENTER);

        DefaultTableModel mytbmodel = (DefaultTableModel) tblProduto.getModel();
        ProdutoDaoDef u1 = new ProdutoDaoDef();

        resul = u1.buscartodos();
        try {
            // apaga todas as linhas do Jtable
            while (tblProduto.getModel().getRowCount() > 0) {
                ((DefaultTableModel) tblProduto.getModel()).removeRow(0);
            }

            while (resul.next()) {
                Object[] linhas = {
                    resul.getInt("IdProduto"),
                    resul.getInt("Fk_idCategoria"),
                    resul.getString("produto"),
                    resul.getString("tipo"),
                    resul.getInt("quantidade"),
                    resul.getFloat("vlCompra"),
                    resul.getFloat("icms"),
                    resul.getFloat("vlVenda")};

                // adiciona uma linha de cada vez no jtable de usua?ios
                mytbmodel.addRow(linhas);
            }

            // posiciona o focus no campo 
            this.textProduto.grabFocus();
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar produto\n" + err.getMessage());
        }
    }

