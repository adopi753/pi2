    private void cadastrarProduto() {
        //Instancia a Classe "UsuarioDef" e cria o objeto "Lu" - Pacote "Model"
        ProdutoDef cad = new ProdutoDef();

        //usa o objeto "cad" para adiconar os valores na classe "ProdutoDef" - Pacote "Model"            
        cad.setFk_idCategoria(this.cmbCategoria.getSelectedIndex());
        cad.setProduto(this.textProduto.getText());
        cad.setTipo(this.textTpProduto.getText());
        cad.setQuantidade(Integer.parseInt(this.textQtProduto.getText()));
        cad.setVlCompra(Float.parseFloat(this.textVlCompraProd.getText()));
        cad.setIcms(Float.parseFloat(this.textIcmsProd.getText()));
        cad.setVlVenda(Float.parseFloat(this.textVlVendaProd.getText()));

        //Instancia a Classe "UsuarioDaoDef"
        ProdutoDaoDef u1 = new ProdutoDaoDef();
        //executa a função "incluir" da classe "UsuarioDaoDef"        
        u1.incluir(cad);// se não existir chama o u1.incluir()
        this.carregarProduto();
        this.camposBotoesInicioCadastro();

        this.limparCampos();
        this.textProduto.grabFocus(); //coloca o cursor no campo nome        
    }

    private void alterarProduto() {
        //Instancia a Classe "UsuarioDef" e cria o objeto "Lu" - Pacote "Model"
        ProdutoDef cad = new ProdutoDef();

        //usa o objeto "cad" para adiconar os valores na classe "ProdutoDef" - Pacote "Model"    
        cad.setIdProduto(Integer.parseInt(this.textIdProduto.getText()));
        cad.setFk_idCategoria(this.cmbCategoria.getSelectedIndex());
        cad.setProduto(this.textProduto.getText());
        cad.setTipo(this.textTpProduto.getText());
        cad.setQuantidade(Integer.parseInt(this.textQtProduto.getText()));
        cad.setVlCompra(Float.parseFloat(this.textVlCompraProd.getText()));
        cad.setIcms(Float.parseFloat(this.textIcmsProd.getText()));
        cad.setVlVenda(Float.parseFloat(this.textVlVendaProd.getText()));

        //Instancia a Classe "UsuarioDaoDef"
        ProdutoDaoDef u1 = new ProdutoDaoDef();
        //executa a função "incluir" da classe "UsuarioDaoDef"
        /* Para verificar se o usuário existe buscamos o id primeiro */
        resul = u1.buscar(cad);
        try {
            if (resul.next()) {
                u1.alterar(cad);// caso exista chama u1.alterar()
                this.carregarProduto();
                this.camposBotoesInicioCadastro();
            } else {
                this.camposBotoesInicioCadastro();
                btnEditar.setEnabled(false);
            }

        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null,
                    "Erro ao Cadastrar Produto!\n" + err.getMessage());
        }

        this.limparCampos();
        this.textProduto.grabFocus(); //coloca o cursor no campo nome        
    }

    private void excluirProduto() {
        Object[] options = {"Sim", "Não"};
        int i = JOptionPane.showOptionDialog(null, "Deseja realmente excluir o registro?", "Aviso",
                JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,
                null, options, options[0]);
        if (i == 0) {
            ProdutoDaoDef u = new ProdutoDaoDef();
            u.excluir(textIdProduto.getText());
            
            this.limparCampos();
            this.textProduto.grabFocus(); //coloca o cursor no campo cpf
            this.carregarProduto();
        }
    }

    private void pesquisarProduto() {
        // instancia a classe UsuarioProj model
        ProdutoDef obj = new ProdutoDef();
        obj.setIdProduto(Integer.parseInt(this.textIdProduto.getText()));
        ProdutoDaoDef u1 = new ProdutoDaoDef();
        resul = u1.buscar(obj);
        try {
            if (resul.next()) {
                // se o registro foi encontrado preenche os campos do formulário CadProdutoDef
                this.cmbCategoria.setSelectedIndex(resul.getInt("fk_idCategoria"));
                this.textProduto.setText(resul.getString("produto"));
                this.textTpProduto.setText(resul.getString("tipo"));
                this.textQtProduto.setText(String.valueOf(resul.getInt("quantidade")));
                this.textVlCompraProd.setText(String.valueOf(resul.getFloat("vlCompra")));
                this.textIcmsProd.setText(String.valueOf(resul.getFloat("icms")));
                this.textVlVendaProd.setText(String.valueOf(resul.getFloat("vlVenda")));

                this.botoesPesquisarProduto();

            } else {
                // se não encontrar manda mensagem abaixo
                JOptionPane.showMessageDialog(null, "Registro não encontrado!");
                this.limparCampos();
                this.textIdProduto.grabFocus();
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }

    private void clickJtable() {
        // busca a linha selecionada no jTable1
        int linhasel = this.tblProduto.getSelectedRow();
        int colunachave = 0; // configura coluna como 0, isto é, a primeira coluna
        Object chave = this.tblProduto.getModel().getValueAt(linhasel, colunachave);

        ProdutoDef obj = new ProdutoDef();
        obj.setIdProduto((int) chave);
        ProdutoDaoDef u1 = new ProdutoDaoDef();
        resul = u1.buscar(obj);
        try {
            if (resul.next()) {
                this.textIdProduto.setText(String.valueOf(resul.getInt("idProduto")));
                this.cmbCategoria.setSelectedIndex(resul.getInt("fk_idCategoria"));
                this.textProduto.setText(resul.getString("produto"));
                this.textTpProduto.setText(resul.getString("tipo"));
                this.textQtProduto.setText(String.valueOf(resul.getInt("quantidade")));
                this.textVlCompraProd.setText(String.valueOf(resul.getFloat("vlCompra")));
                this.textIcmsProd.setText(String.valueOf(resul.getFloat("icms")));
                this.textVlVendaProd.setText(String.valueOf(resul.getFloat("vlVenda")));

                this.camposBotoesClickJtable();
            } else {
                JOptionPane.showMessageDialog(null, "Registro não encontrado!");
                this.limparCampos();
                this.textIdProduto.grabFocus();
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Erro ao navegar na Jtable! ");
        }
    }

    private void sairProduto() {
        Object[] options = {"Sim", "Não"};
        int i = JOptionPane.showOptionDialog(null, "Deseja realmente sair?", "Aviso",
                JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,
                null, options, options[0]);
        if (i == 0) {
            dispose();
        }
    }
